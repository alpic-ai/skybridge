// Precompile Handlebars templates at framework build time so the runtime can
// import them as plain ESM and we drop `readFileSync` + `fileURLToPath` from
// the production code path (workerd bundles have neither a usable
// `import.meta.url` nor the `.hbs` source files).
import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { basename, join } from "node:path";
import Handlebars from "handlebars";

const ROOT = join(import.meta.dirname, "..");
const TEMPLATES_DIR = join(ROOT, "src", "server", "templates");
const OUTPUT_FILE = join(ROOT, "src", "server", "templates.generated.ts");

const hbsFiles = readdirSync(TEMPLATES_DIR)
  .filter((f) => f.endsWith(".hbs"))
  .sort();

const lines = [
  "// AUTO-GENERATED by scripts/precompile-templates.mjs. Do not edit.",
  // @ts-nocheck on the precompiled bodies — handlebars/runtime is missing the
  // type for `.template()` and the precompile output is intentionally untyped.
  "// @ts-nocheck",
  'import Handlebars from "handlebars/runtime.js";',
  "",
];

for (const file of hbsFiles) {
  const name = basename(file, ".hbs");
  const source = readFileSync(join(TEMPLATES_DIR, file), "utf-8");
  const precompiled = Handlebars.precompile(source);
  lines.push(
    `export const ${name}Template: HandlebarsTemplateDelegate = Handlebars.template(${precompiled});`,
  );
}

writeFileSync(OUTPUT_FILE, `${lines.join("\n")}\n`);
console.log(`Precompiled ${hbsFiles.length} template(s)`);
